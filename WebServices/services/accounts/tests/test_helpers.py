from django.test import TestCase
from services.accounts import utils


class HelpersTest(TestCase):

    inp_guo = {"xxx": "aaa", "op_43": "verify", "opX_40": "wrong"}
    out_guo = {"43": "verify"}
    
    def test_get_user_operations(self):
        """
        Function test.
        
        This test checks whether the given function retrieves properly all
        user_id and operations from the given POST request, as it should be
        generated by the PendingRegView.
        
        """
        result = utils.get_user_operations(self.inp_guo)
        self.assertItemsEqual(result, self.out_guo, "Wrong result!")